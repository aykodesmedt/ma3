  <p class="p--isb-uitleg">
    Internationaal Straattheaterfestival Beveren is een evenement
    opgericht door <span class="link"><a href="http://beverse-feesten.be/">Beveren Verbindt</a></span>. Dit jaar vind de <span class="page--highlight">13e editie</span> van dit
    evenement plaats. Dankzij het succes van de vorige editie blijft de
    locatie van het evenement hetzelfde dit jaar. <br/> <br/>ISB zorgt voor <span class="page--highlight">humor,
    (lucht)acrobatie, vuuracts en stuntwerk</span> van de bovenste plank. Deze
    acts worden uitgevoerd door groepen die uit wel <span class="page--highlight">10 verschillende
    landen </span> komen: België, Nederland, Duitsland, Frankrijk, Spanje, Italië,
    Groot-Brittanië, Argentinië, Guatemala en Canada.
  </p>
