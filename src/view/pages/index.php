      <section class="over_isb">
        <h3 class="over_isb_titel"><span>I</span><span>S</span><span>B</span></h3>
        <p class="over_isb_uitleg">
          Internationaal Straattheaterfestival Beveren is een evenement
          opgericht door Beveren Verbindt. Dit jaar vind de 13e editie van dit
          evenement plaats. Dankzij het succes van de vorige editie blijft de
          locatie van het evenement hetzelfde dit jaar. <br/> <br/>ISB zorgt voor humor,
          (lucht)acrobatie, vuuracts en stuntwerk van de bovenste plank. Deze
          acts worden uitgevoerd door groepen die uit wel 10 verschillende
          landen komen: België, Nederland, Duitsland, Frankrijk, Spanje, Italië,
          Groot-Brittanië, Argentinië, Guatemala en Canada.
        </p>
